				((Control)lstErrors).set_Height(((Control)this).get_Height() - 70);
			}
			if (((Control)this).get_Width() > 300)
			{
				((Control)lstErrors).set_Width(((Control)this).get_Width() - 30);
			}
		}

		private void DisplayErrors_Load(object sender, EventArgs e)
		{
