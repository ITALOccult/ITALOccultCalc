		internal string CollectEvents()
		{
			StringBuilder stringBuilder = new StringBuilder();
			for (int i = 0; i < lstVelocities.get_Items().get_Count(); i++)
			{
				stringBuilder.Append(lstVelocities.get_Items().get_Item(i).ToString() + "\r\n");
			}
			return stringBuilder.ToString();
		}

		private void copyToolStripMenuItem_Click(object sender, EventArgs e)
		{
			Clipboard.SetText(CollectEvents());
		}

		private void saveToolStripMenuItem_Click(object sender, EventArgs e)
		{
			Output.SavePredictionText(CollectEvents(), "ObserverVelocities", Utilities.AppPath + "\\Asteroid");
		}

		private void printToolStripMenuItem_Click(object sender, EventArgs e)
		{
