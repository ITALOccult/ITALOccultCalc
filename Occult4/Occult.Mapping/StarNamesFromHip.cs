namespace Occult.Mapping
{
	internal class StarNamesFromHip
	{
		private int hip;

		private string name;

		internal int Hip
		{
			get
			{
				return hip;
			}
			set
			{
				hip = value;
			}
		}

		internal string Name
		{
			get
			{
				return name;
			}
			set
			{
				name = value;
			}
		}
	}
}
